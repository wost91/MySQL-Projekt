#Utworzenie bazy

create database Pracownicy;
use Pracownicy;

create table Pracownicy (ID int primary key auto_increment, imie varchar(45), nazwisko varchar(45), PESEL VARCHAR(11) UNIQUE, Data_ur date);
create table Pracownik_Dzial (ID int primary key auto_increment, dzial enum('sprzedaż','IT','administracja','obsługa klienta'));
create table Dzial (dzial enum('sprzedaż','IT','administracja','obsługa klienta') primary key, budzet float, liczba_pracownikow int);
create table Dyrektorzy (dyrektor varchar(45) primary key, dzial enum('sprzedaż','IT','administracja','obsługa klienta'));
create table Pracownik_Stanowisko (ID int primary key auto_increment, stanowisko enum('specjalista','starszy specjalista','menedżer'));
create table Stanowisko_Pensja (stanowisko enum('specjalista','starszy specjalista','menedżer') primary key, pensja float);
create table Kwalifikacja_do_premii (ID int primary key auto_increment, kwalifikacja enum('tak','nie'));
create table Staż_w_firmie (ID int primary key auto_increment, staz int);
create table Wysokość_premii (staz int primary key, premia float);
create table Wykształcenie (ID int primary key auto_increment, uczelnia varchar(45), data_rozp date, data_zak date, tytul enum('licencjat','inżynier','magister','doktor'));
create table Historia_zatrudnienia (ID int primary key auto_increment, poprz_pracodawca varchar(45), data_zatrudnienia date, data_odejscia date);
create table Logowanie (ID int primary key auto_increment, login varchar(45), haslo varchar(45));

